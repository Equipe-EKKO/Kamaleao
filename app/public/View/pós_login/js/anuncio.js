function abreModal() {
  $("#md-pr").removeClass("md-ct-pos").addClass("md-ct"); 
  $("#md-pedido").removeClass("md-ct-pos").addClass("md-pedido"); 
}

function fechaModal() {
  $("#md-pr").removeClass("md-ct").addClass("md-ct-pos"); 
  $("#md-pedido").removeClass("md-pedido").addClass("md-ct-pos"); 
}